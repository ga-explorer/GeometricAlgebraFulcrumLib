        private void InitializeWebBrowser()
        {
            var localFilePath =
                Path.Combine(
                    Application.StartupPath,
                    @"Assets\html-resources\xeogl\xeoglTemplate.html"
                );

            if (!File.Exists(localFilePath))
            {
                MessageBox.Show(
                    @"Error: The local html file doesn't exists: " + 
                    Environment.NewLine + 
                    localFilePath
                );
            
                return;
            }

            var webBrowserSettings = new CefSettings();

            Cef.Initialize(webBrowserSettings);

            _webBrowser = new ChromiumWebBrowser(localFilePath);

            panelWebBrowser.Controls.Add(_webBrowser);
            _webBrowser.Dock = DockStyle.Fill;

            // Allow the use of local resources in the browser
            var browserSettings = new BrowserSettings
            {
                FileAccessFromFileUrls = CefState.Enabled, 
                UniversalAccessFromFileUrls = CefState.Enabled
            };

            _webBrowser.BrowserSettings = browserSettings;
        }


