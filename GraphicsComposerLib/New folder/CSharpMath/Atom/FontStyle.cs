    Caligraphic,
    ///<summary>Monospace, i.e. \mathtt</summary>
    Typewriter,
    ///<summary>\mathit</summary>
    Italic,
    ///<summary>\mathss</summary>
    SansSerif,
    ///<summary>\mathfrak</summary>
    Fraktur,
    ///<summary>\mathbb</summary>
    Blackboard,
    ///<summary>\mathbfit</summary>
    BoldItalic
  }
}