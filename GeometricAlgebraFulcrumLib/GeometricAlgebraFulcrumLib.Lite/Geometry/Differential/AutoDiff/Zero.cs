namespace GeometricAlgebraFulcrumLib.Lite.Geometry.Differential.AutoDiff;

/// <summary>
/// A constant zero term. Similar to <see cref="Constant"/> but represents only the value 0.
/// </summary>
#if (!NETSTANDARD1_0 && !NETSTANDARD1_1 && !NETSTANDARD1_2 && !NETSTANDARD1_3 && !NETSTANDARD1_4 && !NETSTANDARD1_5 && !NETSTANDARD1_6)
[Serializable]
#endif
public class Zero : Term
{
    /// <inheritdoc />
    public override void Accept(ITermVisitor visitor)
    {
        Guard.NotNull(visitor, nameof(visitor));
        visitor.Visit(this);
    }

    /// <inheritdoc />
    public override TResult Accept<TResult>(ITermVisitor<TResult> visitor)
    {
        Guard.NotNull(visitor, nameof(visitor));
        return visitor.Visit(this);
    }
}